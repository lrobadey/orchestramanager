export function createInitialAudience(): AudienceState {
  return {
    relationships: cityAudienceSegments.map(segment => {
      const defaults: Record<string, Pick<AudienceState['relationships'][number], 'awareness' | 'trust' | 'habit'>> = {
        'classical-core': { awareness: 18, trust: 10, habit: 2 },
        'new-music-public': { awareness: 9, trust: 8, habit: 0 },
        'cultural-omnivores': { awareness: 7, trust: 6, habit: 0 },
        'students-emerging-artists': { awareness: 6, trust: 7, habit: 0 },
        'civic-tech-professionals': { awareness: 4, trust: 5, habit: 0 },
        'community-neighborhood-public': { awareness: 2, trust: 5, habit: 0 },
      }
      const seed = defaults[segment.id] ?? { awareness: 4, trust: 5, habit: 0 }
      return {
        segmentId: segment.id,
        ...seed,
        alignmentMemory: 0,
        recentReaction: 'Has not yet formed an opinion of the orchestra.',
        lastDelta: 0,
      }
    }),
  }
}

